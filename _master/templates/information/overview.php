<h3>What is an RSS Feed?</h3>
<p>RSS is a family of Web feed formats used to publish frequently updated content such as blog entries, news headlines, and podcasts in a standardized format. An RSS document (which is called a "feed", "web feed", or "channel") contains either a summary of content from an associated web site or the full text. RSS makes it possible for people to keep up with web sites in an automated manner that can be piped into special programs or filtered displays.
<div style="font-style:italic; text-align: right;">&mdash;Definition courtesy of <a href="http://en.wikipedia.org/wiki/RSS_(file_format)" target="_blank">Wikipedia <img src="/site/images/icons/new_page.png" /></a></div>
</p>
<h3>How do I use them</h3>
<p>You can install a news reader that displays RSS feeds or use a web-based news reader, such as using a personalized version of Yahoo (My Yahoo) or Google (iGoogle). </p>

